        // 2. Inject Raw Sensor Data into Data Memory
        // Our assembly code reads from base address 0x1000. 
        // Because your memory uses word-indexing (addr[11:2]), 0x1000 / 4 = Index 1024.
        dut.d_mem.memory[1024] = 32'hFFFFFE0C; // Sensor 1: -500 (Testing CABS)
        dut.d_mem.memory[1025] = 32'h00000020; // Sensor 2: 32   (Testing BITREV)
        dut.d_mem.memory[1026] = 32'hFFFFFFFF; // Sensor 3: -1   (Testing edge cases)

        // Initialize signals
        clk = 0;
        rst = 1;

        // Apply reset for one clock cycle
        #10;
        rst = 0;
        
        // 3. Wait for execution
        // The program loops 3 times and hits an infinite trap. 5000ns is plenty of time.
        #5000;

        // 4. Evaluate the Results in the Network Buffer
        // Our assembly code writes the output to base address 0x2000.
        // 0x2000 / 4 = Index 2048.
        $display("\n====================================================");

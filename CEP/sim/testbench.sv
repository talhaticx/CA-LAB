        $display("====================================================");
        
        // Index 2048: The Header (Generated by MASKI and BSWAP)
        $display("Header      (Mem[2048]) : %08x", dut.d_mem.memory[2048]);
        
        // Indices 2049-2051: The Scrambled Payloads (Generated by CABS, BITREV, SW.BSWAP)
        $display("Scrambled 1 (Mem[2049]) : %08x", dut.d_mem.memory[2049]);

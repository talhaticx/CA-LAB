    single_cycle dut (
        .clk(clk),
        .rst(rst)
    );

    // Clock Generation (10ns period -> 100MHz)
    always #5 clk = ~clk;

    // Test Sequence
    initial begin
        $dumpfile("dump.vcd");
        $dumpvars(0, testbench);

        // 1. Load the Custom Compiled Hex File
        // Adjust the hierarchical path (`dut.instr_mem.memory`) to match your exact RTL.
